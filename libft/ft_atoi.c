int	ft_atoi(const char *str)
{
	int	i;
	int	n;
	int	r;

	i = 0;
	n = 1;
	r = 0;
	while ((str[i] <= 13 && str[i] >= 9) || str[i] == 32)
		i++;
	if (str[i] == 45)
	{
			n *= -1;
			i++;
	}
	else if (str[i] == 43)
		i++;
	while ((str[i] >= 48 && str[i] <= 57) && str[i])
	{	
		r = r * 10 + (str[i] - 48);
		i++;
	}
	r = r * n;
	return (r);
}
